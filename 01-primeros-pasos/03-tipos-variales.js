// En los lenguajes tradicionales 
//     Tipos Primitivos: 
//         - Cadenas (Strings), 
//         - Falso/Verdadero (Booleans),
//         - Enteros (Integer/int)
//         - Punto decimal (Float)
//         - Punto decimal de doble precision (Double)
//     Tipos Complejos
//         Estructuras de datos
//             - Object (Objetos)
//             - Array (vectores)
//             - List (Listas)


// En JavaScript
//      Tipos Primitivos: 
//         - Cadenas (Strings), 
             let name = 'Juan';
//         - Falso/Verdadero (Booleans),
             let isMale = true; 
//         - Numeros (Numbers)
             let age = 46;
             let height = 1.67;
//         - null             (nulo)
             let nullValue = null;
//         - undefined        (Indefinido)
             let undefinedValue = undefined;
//         - NaN              (Not is a Number)
             let otherValue = NaN;

//      Tipos Complejos
//         - Arrays o Lists (Uno no se parece al otro) 'Object'
             let fruits = [ 'orange', 'pinapple', 'banana', 'apple' ];
//         - Objects 'object'
             let persona = {
                name: 'Juan',
                age: 46
             } 